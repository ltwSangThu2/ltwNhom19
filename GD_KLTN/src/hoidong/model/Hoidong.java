package hoidong.model;

public class Hoidong {
	private int idHoidong;
	private String tenHoidong;
	private String chuyenNganh;
	public int getIdHoidong() {
		return idHoidong;
	}
	public void setIdHoidong(int idHoidong) {
		this.idHoidong = idHoidong;
	}
	public String getTenHoidong() {
		return tenHoidong;
	}
	public void setTenHoidong(String tenHoidong) {
		this.tenHoidong = tenHoidong;
	}
	public String getChuyenNganh() {
		return chuyenNganh;
	}
	public void setChuyenNganh(String chuyenNganh) {
		this.chuyenNganh = chuyenNganh;
	}
	
}
